import {Entity, model, property, hasMany} from '@loopback/repository';
import {DetalleFactura} from './detalle-factura.model';

@model()
export class Productos extends Entity {
  @property({
    type: 'string',
    id: true,
    generated: true,
  })
  id?: string;

  @property({
    type: 'string',
    required: true,
  })
  descripcion: string;

  @property({
    type: 'number',
    required: true,
  })
  valor: number;

  @hasMany(() => DetalleFactura)
  detalleFacturas: DetalleFactura[];

  constructor(data?: Partial<Productos>) {
    super(data);
  }
}

export interface ProductosRelations {
  // describe navigational properties here
}

export type ProductosWithRelations = Productos & ProductosRelations;
